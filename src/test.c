typedef void (*setup_func)( void * data, size_t data_sz );
typedef int (*run_test_func)( const char ** testname );
typedef void (*tear_down_func)();

struct test {
	setup_func setup;
	void * data;
	size_t data_sz;
	run_test_func run_test;
	tear_down_func tear_down;
};

#define TEST( buf, func ) { pipe_setup, buf, sizeof buf, func, pipe_teardown }
static struct test tests[] = {
	TEST( short_ascii, 		test_short_ascii_char ),
	TEST( short_utf16le, 	test_short_utf16le_char ),
	TEST( header_ascii, 	test_ascii_longer_char ),
	TEST( header_utf16le, 	test_utf16le_longer_char ),
	TEST( short_ascii, 		test_short_lex),
	TEST( short_utf16le, 	test_short_lex),
	TEST( header_ascii, 	test_header_lex),
	TEST( header_utf16le, 	test_header_lex),
	TEST( short_ascii_csv, 	test_lex_short_csv),
	{ NULL }
};

void run_tests() {
	int test_count = 0;
	int passing_test_count = 0;

	struct test * one_test = tests;

	for(; one_test->setup ; one_test++ ) {
		test_count++;

		if(one_test->setup) 
			one_test->setup( one_test->data, one_test->data_sz );

		if( !one_test->run_test ) {
			fwprintf( stderr, L"Hmm, an empty test, skipping...\n");
			continue;
		}

		const char * testname;
		if(one_test->run_test( &testname )) {
			passing_test_count++;
		} else {
			fwprintf( stderr, L"Test failed: %s\n", testname );
		}

		if(one_test->tear_down) one_test->tear_down();
	}
	fwprintf(stderr, L"%d of %d test(s) passed\n", passing_test_count, test_count );
}

